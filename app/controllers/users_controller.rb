class UsersController < ApplicationController
  # GET /users
  # GET /users.xml
  
   layout 'main'
  
  def index
  	session[:title]='Lista de usuarios'
    session[:image]='members.png'
  	
    @users = User.all

    respond_to do |format|
      format.html # index.html.erb
      format.xml  { render :xml => @users }
    end
  end

  # GET /users/1
  # GET /users/1.xml
  def show
    @user = User.find(params[:id])

    respond_to do |format|
      format.html # show.html.erb
      format.xml  { render :xml => @user }
    end
  end

  # GET /users/new
  # GET /users/new.xml
  def new
    @user = User.new

    respond_to do |format|
      format.html # new.html.erb
      format.xml  { render :xml => @user }
    end
  end
  
  def authenticate
  	@user=User.findByUsername(params[:username])
  	if @user
  		if @user.password==params(:password)
  			session[:access_granted]=true
  			session[:user]=@user
  			flash[:notice]='Bienvenido #{@user.name}'
  			redirect_to @user.landing_page
  		else
  			session[:access_granted]=false
  			flash[:notice]='Los datos son incorrectos'
  			redirect_to :back
  		end
  	else
  		#NO SE ENCONTRO EL USERNAME
  		session[:access_granted]=false
  		flash[:notice]='Datos de conexiÃ³n incorrectos'
  		redirect_to :back
  	end #end del si existe el user
  	
  end

  # GET /users/1/edit
  def edit
    @user = User.find(params[:id])
  end

  # POST /users
  # POST /users.xml
  def create
    @user = User.new(params[:user])

    respond_to do |format|
      if @user.save
        flash[:notice] = 'User was successfully created.'
        format.html { redirect_to(@user) }
        format.xml  { render :xml => @user, :status => :created, :location => @user }
      else
        format.html { render :action => "new" }
        format.xml  { render :xml => @user.errors, :status => :unprocessable_entity }
      end
    end
  end

  # PUT /users/1
  # PUT /users/1.xml
  def update
    @user = User.find(params[:id])

    respond_to do |format|
      if @user.update_attributes(params[:user])
        flash[:notice] = 'User was successfully updated.'
        format.html { redirect_to(@user) }
        format.xml  { head :ok }
      else
        format.html { render :action => "edit" }
        format.xml  { render :xml => @user.errors, :status => :unprocessable_entity }
      end
    end
  end

  # DELETE /users/1
  # DELETE /users/1.xml
  def destroy
    @user = User.find(params[:id])
    @user.destroy

    respond_to do |format|
      format.html { redirect_to(users_url) }
      format.xml  { head :ok }
    end
  end
end
